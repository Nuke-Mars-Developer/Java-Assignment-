package com.appxstudioinc;

public class practise_Set_5_harry {
    public static void main(String[] args) {
        // Practice set for loops for , while, do while...

        // Problem 1
        // Print star pattern in reverse order
        int n = 4;
        for(int i = 0;i<n;i++)
        {
            for (int j=0;j>n;j++)
            {
                System.out.println("*");
            }
            System.out.println("\n");

        }




    }
}